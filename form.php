<?php
// Перевірка, чи форма була відправлена методом POST
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Отримання номеру телефону з форми
    $phoneNumber = $_POST["phoneNumber"];

    // Ви можете провести валідацію чи обробку тут

    // Встановлення адреси електронної пошти отримувача
    $to = "yaroslav.kond@gmail.com";

    // Встановлення теми для листа
    $subject = "Нова подача форми";

    // Складання тексту листа
    $message = "Номер телефону: " . htmlspecialchars($phoneNumber);

    // Встановлення додаткових заголовків
    $headers = "From: ваша_адреса@example.com\r\n";
    $headers .= "Reply-To: ваша_адреса@example.com\r\n";
    $headers .= "Content-Type: text/html\r\n";

    // Надсилання листа
    if (mail($to, $subject, $message, $headers)) {
        // Якщо лист був успішно надіслано, вивести спливаюче повідомлення
        echo "<script>alert('Дякуємо за відправку форми!');</script>";
    } else {
        // Якщо виникла помилка під час надсилання, можна вивести відповідне повідомлення
        echo "<script>alert('Помилка під час надсилання форми. Спробуйте ще раз.');</script>";
    }
} else {
    // Якщо форма не була відправлена, можна вивести повідомлення про помилку або перенаправити користувача
    echo "Форма не відправлена.";
}
?>
